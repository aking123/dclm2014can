<p>God&rsquo;s infallible WORD teaches and we believe:</p>
<h3>THE HOLY BIBLE</h3>
<p>1. That the Holy Bible, consisting of 39 books of the Old Testament and 27 books of the New Testament, is the inspired Word of God. We take the Bible as final authority in all matters concerning Christian conduct and works. 2 Timothy 3:16,17; Proverbs 30:5,6; Revelation 22:18,19.</p>
<h3>THE GODHEAD</h3>
<p>2. That the Godhead consists of three separate, distinct, and recognizable personalities and qualities, perfectly united in one. The Father, the Son, and the Holy Ghost are different Persons in the Godhead, not merely three names for one Person. Mathew 3:16, 17; 2 Corinthians 13:14; Mathew 28:19,20.</p>
<div>
<div>
<h3>VIRGIN BIRTH OF CHRIST</h3>
<p>3. The virgin birth of Jesus, the only begotten Son of God. The crucifixion, death, burial and bodily resurrection of Jesus Christ. Isaiah 7:14; Mathew 1:18-25; Romans 1:4; 1 Corinthians 15:3,4.</p>
</div>
</div>
<div>
<div>
<h3>TOTAL DEPRAVITY</h3>
<p>4. The total depravity sinfulness and guilt of all men since the Fall, rendering them subject to God&#8217;s wrath and condemnation. Psalm 51:5; Job 14:4; Romans 3:23; 5:12-17; Mark 7:21-23; Ephesians 2:1.</p>
</div>
</div>
<h3>REPENTANCE</h3>
<p>5. That repentance is a complete turning away from all sins and its deceitful pleasures and that it is required from every sinner before he can truly and effectively believe in Jesus with saving faith. Proverbs 28:13; Isaiah 55:7; Ezekiel 18:21-23; Mark 1:15; Luke 24:46,47; Acts 2:38; 3:19,21; 2 Corinthians 7:10; Hebrews 6:1-3.</p>
<h3>RESTITUTION</h3>
<p>6. That restitution is making amends for wrongs done against our fellow men, restoring stolen things to the rightful owners, paying debts, giving back where one has defrauded, making confessions to the offended and apologizing to those slandered so as to have a conscience void of offence towards God and man. Genesis 20:1-8; 14-18; Exodus 22:1-7; Leviticus 6:1-17; Numbers 5:6-8; 2 Samuel 12:1-6; Proverbs 6:31; Ezekiel 33:14-16; Mathew 23:24; Luke 19:8,9; Acts 23:1-5; 24:16; James 4:17; 5:6.</p>
<h3>JUSTIFICATION</h3>
<p>7. That justification (or Regeneration) is the act of God&#8217;s grace whereby one receives forgiveness and remission of sins and is counted righteous before God, through faith in the atoning blood of Jesus, standing before God as though he had never sinned. Psalms 32:1,2; Isaiah 1:18; Micah 7:19; John 3:3-8;; Acts 13:38,39; Romans 3:24-30; 4:6-16; 5:1,2; 2 Corinthians 5:17-21; Galatians 2:16; Ephesians 2:8,9; Titus 3:5; 1 Peter 1:23.</p>
<h3>WATER BAPTISM</h3>
<p>8. That Water Baptism is essential to our obedience after reconciliation with God. Water Baptism is one immersion (not three) &#8220;In the name of the Father, and of the Son, and of the Holy Ghost&#8221; as Jesus commanded. Mathew 28:19; 3:13-17; Mark 16:15; Acts 2:38,39; 19:1-6; Romans 6:4,5.</p>
<div>
<div>
<h3>LORD&#8217;S SUPPER</h3>
<p>9. That the Lord&#8217;s Supper was constituted by Jesus Christ so that all believers (all members of the family of God) might partake thereof regularly, to &#8220;shew the Lord&#8217;s death till He come&#8221;. The emblems used are &#8220;unleavened bread&#8221; and the juice of &#8220;fruit of the vine&#8221;. Anyone who eats and drinks unworthily brings &#8220;damnation&#8221;, punishment and chastisement upon himself. Mathew 26:26-29; Luke 22:17-20; 1 Corinthians 11:23-30.</p>
</div>
</div>
<div>
<div>
<h3>SANCTIFICATION</h3>
<p>10. That Entire Sanctification is a definite act of God&#8217;s grace, subsequent to the New Birth, by which the believer&#8217;s heart is purified and made holy. It cannot be attained progressively by works, struggle or suppression, but is obtained by faith in the sanctifying blood of Jesus Christ. Holiness of life and purity of heart are central to Christian living. Luke 1:74,,75; John 17:15-17; 1 Thessalonians 4:3,7,8; 5:22-24; Ephesians 5:25-27; Hebrews 2:11; 10:10,14; 13:11,12; Titus 2:11-14; 1 John 1:7; Hebrews 12:14, 1 Peter 1:14-16.</p>
</div>
</div>
<h3>HOLY GHOST BAPTISM</h3>
<p>11. That the Baptism in the Holy Ghost is the enduement of power from on High upon the sanctified believer. It is &#8220;the promise of the Father&#8221; and when one receives this &#8220;gift of the Holy Ghost&#8221;, it is accompanied by the initial evidence of speaking a language unlearned previously, referred to as speaking in tongues as the Spirit gives utterance. Mathew 3:11; Acts 1:88; Luke 3:16; 24:49; John 1:30-33; 7:37-39; 14:16,17,26; 16:12-15; Acts 1:5-8; Mark 16:17; Acts 2:1-18; 10:44-46; 19:1-6.</p>
<p>We do not teach or instruct people how to speak in tongues; the Holy Spirit gives utterance. We also stress the necessity of Purity before Power. The Gifts of the Spirit are for today. 1 Corinthians 12:1-31; 14:1-40.</p>

<h3>REDEMPTION</h3>
<p>12. That Redemption from the curse of the law, Healing of sickness and disease as well as continued Health are provided for all people through the sacrificial death of Jesus Christ. Exodus 15:26; Deuteronomy 7:15; Psalms 103:1-5; Proverbs 4:20-22; Isaiah 8:16,17; 1 Peter 2:24; Mark 16:15-18; Luke 13:16; John 14:12-14; 10:10; Acts 10:38; James 5:14-16; 1 John 3:8; 3 John 2; Galatians 3:13,14.</p>

<h3>PERSONAL EVANGELISM</h3>
<p>13. That Personal Evangelism is a God-given and God-ordained ministry for every believer. Jesus commanded and God requires every believer to be a compassionate and fruitful soul winner, bringing others to Christ. Mathew 28:19,20; Mark 16:15; Luke 24:46-49; John 17:18; Acts 1:8; 8:1-4; Psalms 126:5,6; Proverbs 11:30; Daniel 12:3; Ezekiel 3:17-21.</p>
<h3>MARRIAGE</h3>
<p>14. That Marriage is binding for life. Monogamy is the uniform teaching of the Bible. Polygamy is contrary to God&#8217;s perfect will and institution. The Bible forbids a believer marrying an unbeliever. Also, under the New Testament dispensation, no one has a right to divorce and remarry while the first companion lives. When a person becomes converted, necessary restitution, on this line must be done without delay if he has married wrongly. Genesis 2:24; Deuteronomy 7:1-4; Joshua 23:11-13; 2 Corinthians 6:14-18; Proverbs 31:10-31; Malachi 2:14-15; Romans 7:2,3; Ephesians 5:31-33; Mathew 5:31,32; 19:3-9; Mark 10:2-12; Luke 16:18; John 4:15-19; Genesis 20:3,7.</p>
<h3>RAPTURE</h3>
<p>15. That the Rapture (the first phase or stage of the SECOND COMING OF CHRIST) is the catching away from the earth of all living saints; and all who died in the Lord. The Rapture will take place before the Great Tribulation and can happen any time from now. &#8220;In a twinkling of an eye&#8221; without a moment&#8217;s warning, &#8220;the trumpet shall sound&#8221;, &#8220;and the dead in Christ shall rise first then we which are alive and remain shall be caught up together with them in the clouds, to meet the Lord in the air; and so shall we ever be with the Lord.&#8221; John 14:1-3; Luke 21:34-36; 1 Corinthians 15:51-58; 1 Thessalonians 4:13-18; 5:4-9; 2 Thessalonians 2:5-7; Philippians 3:11,20,21; 1 John 3:1-3.</p>
<h3>RESURRECTION OF THE DEAD</h3>
<p>16. That the Resurrection of the dead is taught in the Bible as clearly as the immortality of the soul. Every individual who has ever lived will be resurrected, some to honor and glory and others to everlasting shame and contempt. Job 19:25-27; Psalm 71:20; Isaiah 26:19; Daniel 12:2; John 5:28,29; 1 Corinthians 15:12-57; 1 Thessalonians 4:13-16; Hebrews 6:1,2; Philippians 3:8-11; Revelation 20:4,6,12,13.</p>
<h3>GREAT TRIBULATION</h3>
<p>17. That the Great Tribulation will occur after the Rapture and will be a time of terrible suffering on earth. It is also referred to as the time of Jacob&#8217;s trouble. Mathew 24:21,22,29; Revelation 9:16; Mark 13:19; 2 Thessalonians 2:3-12; Revelation 13. During this time, the Antichrist will take possession of this world for a reign of terror. He will not be a system or organization but a person &#8211; a supernatural diabolical being, in the form of a man who will blaspheme and proclaim himself to be God (Daniel 8:23,25; 2 Thessalonians 2:7-12; Revelation 13:1-10). The Marriage Supper of the Lamb will take place above while the tribulation continues on earth Revelation 19:1-10.</p>
<div>
<div>
<h3>SECOND COMING OF CHRIST</h3>
<p>18. The Second Coming of Christ will be just as literal and visible as His going away, and He is coming to execute judgment upon the ungodly. He will also, then, set up His Kingdom and reign on this present earth for a thousand years. Zechariah 14:3,4; Mathew 25:31-46; Mark 13:24-37; 2 Thessalonians 1:7-10; 2:8; Jude 14,15.</p>
</div>
</div>
<div>
<div>
<h3>CHRIST&#8217;S MILLENNIAL REIGN</h3>
<p>19. That Christ&#8217;s Millennial Reign is the 1,000 years&#8217; literal reign of Jesus on earth, which will be ushered in by the coming of Jesus back to earth with ten thousands of His saints. At this time He will judge the nations that dwell upon the face of the earth. Jude 14,15; 2 Thessalonians 1:7-10. During this time the devil will be bound. Revelation 20:2,3. It will be a reign of peace and blessing. Isaiah 11:6-9; 65:25; Hosea 2:18; Zechariah 14:9-20; Isaiah 2:2-4.</p>
</div>
</div>
<h3>GREAT WHITE THRONE JUDGEMENT</h3>
<p>20. That the Great White Throne Judgment is when God finally judges all (the living and the dead, small and great) who have ever lived on the face of the earth, according to their works. This is after the Millennium. At this time, the final Judgment known as the Great White Throne Judgment, will be held. All those from all ages, who have not yet been judged (believers&#8217; judgment, as sinners was accomplished by Christ on the Cross &#8211; John 5:24; 3:17-19) will stand before God at this time. The devil and his angels are judged at this time also and sent to the lake of fire forever. Dan 12:2,3; Matt 10:15; 11:21-24; 12:41,42; John 5:28,29; Rom 2:15,16; 14:12; 2 Pet 2:9; Jude 6; 1 Cor 6:1-4; Acts 10:42; Rev 20:11-15.</p>
<h3>NEW HEAVEN AND THE NEW EARTH</h3>
<p>21. That the New Heaven and the New Earth &#8220;wherein dwelleth righteousness&#8221; will be made by God and the redeemed shall dwell with God forever. This present earth, which has been polluted by sin, will pass away after the Great White Throne Judgment (Psalm 102:25,26; Isaiah 51:6; 65:17; Mathew 5:18; 24:35; 2 Peter 3:10-13; Revelation 21:1). No unclean thing will be there. We shall know each other, our knowledge having been perfected. There will be no more curses upon anything. There will be no more night, the glory of the Lord will be the light thereof. Isaiah 66:22; 2 Peter 3:12,13; 1 Cor 13:12; 1 John 3:2,3; Rev 21:1-7; 22:1-5.</p>
<h3>HELL FIRE</h3>
<p>22. That Hell-fire is a place of everlasting punishment where sinners (all who do not have their names in the book of life) will suffer torments forever and ever. It was prepared for the devil and his angels (Mathew 25:41) but God has decreed that the wicked and those who forgets Him and reject Christ will also be cast there because of their sin and neglect of His salvation. Ps 9:17; Mt 25:46; Lk 12:4,5; 16:19-31; Mt 5:22,30; Mk 9:43-47; Rev 14:10,11; 20:12-15.</p>
		

